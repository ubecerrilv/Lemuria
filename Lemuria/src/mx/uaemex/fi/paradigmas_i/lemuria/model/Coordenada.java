package mx.uaemex.fi.paradigmas_i.lemuria.model;

public class Coordenada {
/**************************************************************************************
 * 
 * 									ATRIBUTOS
 * 
*************************************************************************************/
	private int x;
	private int y;
		
/**************************************************************************************
* 
* 									METODOS
* 
*************************************************************************************/
	public Coordenada() {
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}
	
	public void setY(int y) {
		this.y = y;
	}
	
}
